// IPD Charges controller - line-item ledger of all chargeable items during
// admission (medicines, lab, procedures, doctor visits, consumables, etc).
//
// Bed rent is NOT stored here - it's calculated from admittedAt/dischargedAt
// at billing time. All other charges go in this table.
//
// Permission gates set in routes:
//   read   → manageIPD
//   write  → manageIPDBilling (Admin, Doctor, Receptionist)

const prisma = require('../../lib/prisma')
const { successResponse, errorResponse } = require('../../lib/response')

const VALID_TYPES = [
  'BED_RENT', 'DOCTOR_VISIT', 'CONSULTATION', 'NURSING_CARE',
  'MEDICINE', 'CONSUMABLE', 'LAB_TEST', 'IMAGING', 'PROCEDURE',
  'OT_CHARGE', 'PACKAGE', 'PACKAGE_EXTRA',
  'DEPOSIT_REFUND', 'ADJUSTMENT', 'OTHER',
]

async function loadAdmission(req, res, allowClosed = false) {
  const admission = await prisma.admission.findFirst({
    where: { id: req.params.admissionId, clinicId: req.clinicId },
  })
  if (!admission) {
    errorResponse(res, 'Admission not found', 404)
    return null
  }
  // Charges may be added/edited even on a recently-closed admission
  // (final billing happens after discharge), so we don't block by default.
  return admission
}

// ── List charges for an admission ─────────────────────────
// Returns: { charges, summary } where summary aggregates by chargeType and
// totals (excluding voided rows).
async function listCharges(req, res) {
  try {
    const admission = await loadAdmission(req, res, true)
    if (!admission) return

    const { includeVoided = 'false' } = req.query
    const where = { admissionId: admission.id }
    if (includeVoided !== 'true') where.voidedAt = null

    const charges = await prisma.iPDCharge.findMany({
      where,
      orderBy: { chargedAt: 'desc' },
      include: { addedBy: { select: { id: true, name: true } } },
    })

    // Summary by chargeType (active charges only)
    const active = charges.filter(c => !c.voidedAt)
    const byType = {}
    let total = 0
    for (const c of active) {
      byType[c.chargeType] = (byType[c.chargeType] || 0) + (c.amount || 0)
      total += (c.amount || 0)
    }

    return successResponse(res, {
      charges,
      summary: {
        total,
        byType,
        count: active.length,
      },
    })
  } catch (err) {
    console.error('[listCharges]', err)
    return errorResponse(res, 'Failed to fetch charges', 500)
  }
}

// ── Create a charge ───────────────────────────────────────
async function createCharge(req, res) {
  try {
    const admission = await loadAdmission(req, res, true)
    if (!admission) return

    const {
      chargeType, description, quantity = 1, unitPrice, amount,
      chargedAt, medicineId, labTestId, notes,
    } = req.body

    if (!chargeType || !VALID_TYPES.includes(chargeType)) {
      return errorResponse(res, `chargeType must be one of: ${VALID_TYPES.join(', ')}`, 400)
    }
    if (!description?.trim()) return errorResponse(res, 'description is required', 400)

    const qty = parseInt(quantity, 10) || 1
    const unit = parseFloat(unitPrice)
    if (Number.isNaN(unit) || unit < 0) return errorResponse(res, 'unitPrice must be a non-negative number', 400)

    // amount provided OR calculated
    let amt = amount !== undefined ? parseFloat(amount) : unit * qty
    if (Number.isNaN(amt) || amt < 0) return errorResponse(res, 'amount must be a non-negative number', 400)

    const charge = await prisma.iPDCharge.create({
      data: {
        admissionId:     admission.id,
        chargedAt:       chargedAt ? new Date(chargedAt) : new Date(),
        chargeType,
        description:     description.trim(),
        quantity:        qty,
        unitPrice:       unit,
        amount:          amt,
        medicineId:      medicineId || null,
        labTestId:       labTestId || null,
        notes:           notes?.trim() || null,
        addedById:       req.user.id,
        isAutoGenerated: false,
      },
      include: { addedBy: { select: { id: true, name: true } } },
    })

    return successResponse(res, charge, 'Charge added', 201)
  } catch (err) {
    console.error('[createCharge]', err)
    return errorResponse(res, 'Failed to add charge', 500)
  }
}

// ── Update a charge (only non-voided, by adder, within 24h) ──
async function updateCharge(req, res) {
  try {
    const charge = await prisma.iPDCharge.findFirst({
      where: { id: req.params.id, admission: { clinicId: req.clinicId } },
    })
    if (!charge) return errorResponse(res, 'Charge not found', 404)
    if (charge.voidedAt) return errorResponse(res, 'Cannot edit a voided charge', 400)

    const ageMs = Date.now() - new Date(charge.createdAt).getTime()
    if (ageMs > 24 * 3600 * 1000 && charge.addedById !== req.user.id) {
      return errorResponse(res, 'Charges older than 24 hours can only be edited by the original adder', 400)
    }

    const { description, quantity, unitPrice, amount, chargedAt, notes, chargeType } = req.body

    const data = {}
    if (chargeType   !== undefined) {
      if (!VALID_TYPES.includes(chargeType)) return errorResponse(res, 'Invalid chargeType', 400)
      data.chargeType = chargeType
    }
    if (description  !== undefined) data.description = description.trim()
    if (quantity     !== undefined) data.quantity    = parseInt(quantity, 10) || 1
    if (unitPrice    !== undefined) data.unitPrice   = parseFloat(unitPrice)
    if (chargedAt    !== undefined) data.chargedAt   = new Date(chargedAt)
    if (notes        !== undefined) data.notes       = notes?.trim() || null

    // Recalculate amount if quantity or unitPrice changed and amount not explicitly given
    if (amount !== undefined) {
      data.amount = parseFloat(amount)
    } else if ((quantity !== undefined || unitPrice !== undefined)) {
      const newQty  = data.quantity  ?? charge.quantity
      const newUnit = data.unitPrice ?? charge.unitPrice
      data.amount = newQty * newUnit
    }

    const updated = await prisma.iPDCharge.update({
      where: { id: charge.id },
      data,
      include: { addedBy: { select: { id: true, name: true } } },
    })
    return successResponse(res, updated, 'Charge updated')
  } catch (err) {
    console.error('[updateCharge]', err)
    return errorResponse(res, 'Failed to update', 500)
  }
}

// ── Void a charge (soft-delete with reason) ───────────────
// Voided charges are kept for audit but excluded from billing totals.
async function voidCharge(req, res) {
  try {
    const charge = await prisma.iPDCharge.findFirst({
      where: { id: req.params.id, admission: { clinicId: req.clinicId } },
    })
    if (!charge) return errorResponse(res, 'Charge not found', 404)
    if (charge.voidedAt) return errorResponse(res, 'Already voided', 400)

    const { reason } = req.body
    if (!reason?.trim()) return errorResponse(res, 'Void reason is required', 400)

    const updated = await prisma.iPDCharge.update({
      where: { id: charge.id },
      data: {
        voidedAt:   new Date(),
        voidReason: reason.trim(),
      },
    })
    return successResponse(res, updated, 'Charge voided')
  } catch (err) {
    console.error('[voidCharge]', err)
    return errorResponse(res, 'Failed to void', 500)
  }
}

// ── List recently-used charge descriptions for autocomplete ───────────────
// Returns the clinic's previously-used non-voided charge descriptions
// (case-insensitive distinct), ordered by most-recently-used first. Optional
// `?type=MEDICINE` filters to one chargeType (per-type recents). The
// frontend uses this to power the "Description" autocomplete on the charge
// form so receptionists don't keep retyping the same words.
//
// We deliberately compute distinct on the JS side because Postgres + Prisma
// don't expose `DISTINCT ON` cleanly without raw SQL, and the table per
// clinic is small enough (a few thousand rows max) for in-memory dedup.
async function listDescriptions(req, res) {
  try {
    const { type } = req.query
    const where = {
      voidedAt: null,
      admission: { clinicId: req.clinicId },
    }
    if (type && VALID_TYPES.includes(type)) where.chargeType = type

    // Pull recent rows; we'll dedup case-insensitively below.
    const rows = await prisma.iPDCharge.findMany({
      where,
      orderBy: { chargedAt: 'desc' },
      select: { description: true, chargeType: true, unitPrice: true, chargedAt: true },
      take: 400, // cap so a giant ledger doesn't blow up the response
    })

    // Map of lowercased-trimmed key -> first (= most recent) description object
    const seen = new Map()
    for (const r of rows) {
      const key = (r.description || '').trim().toLowerCase()
      if (!key) continue
      if (!seen.has(key)) {
        seen.set(key, {
          description: r.description.trim(),
          chargeType:  r.chargeType,
          unitPrice:   r.unitPrice,        // last-used price - frontend may use as default
          lastUsedAt:  r.chargedAt,
          useCount:    1,
        })
      } else {
        seen.get(key).useCount += 1
      }
    }

    return successResponse(res, Array.from(seen.values()))
  } catch (err) {
    console.error('[listDescriptions]', err)
    return errorResponse(res, 'Failed to fetch descriptions', 500)
  }
}

module.exports = {
  listCharges,
  createCharge,
  updateCharge,
  voidCharge,
  listDescriptions,
}
